function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;

    // Update active state in switcher
    const selector = document.getElementById('languageSelect');
    if (selector) {
        selector.value = lang;
    }

    // Dispatch event for dynamic content updates
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));

    // Update content immediately
    updateContent(lang);
}

function getText(key) {
    const lang = localStorage.getItem('language') || 'en';
    return (translations[lang] && translations[lang][key]) || key;
}

function updateContent(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if ((element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') && element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Special handling for dynamic content if needed
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

function initLanguage() {
    const savedLang = localStorage.getItem('language') || 'en';
    setLanguage(savedLang);
}

// Initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initApp);
} else {
    initApp();
}

function initApp() {
    // Avoid duplicate initialization
    if (document.getElementById('languageSelect')) return;

    // Create Language Switcher Dropdown
    const switcherContainer = document.createElement('div');
    switcherContainer.className = 'language-selector-container';

    const select = document.createElement('select');
    select.id = 'languageSelect';
    select.className = 'language-select';

    const languages = [
        { code: 'en', label: 'ðŸ‡¬ðŸ‡§ English' },
        { code: 'ru', label: 'ðŸ‡·ðŸ‡º Ð ÑƒÑÑÐºÐ¸Ð¹' },
        { code: 'kk', label: 'ðŸ‡°ðŸ‡¿ ÒšÐ°Ð·Ð°Ò›ÑˆÐ°' }
    ];

    languages.forEach(lang => {
        const option = document.createElement('option');
        option.value = lang.code;
        option.textContent = lang.label;
        select.appendChild(option);
    });

    switcherContainer.appendChild(select);

    // Insert into the nav menu or navbar
    const navMenu = document.querySelector('.nav-menu');
    if (navMenu) {
        const li = document.createElement('li');
        li.appendChild(switcherContainer);
        navMenu.appendChild(li);
    } else {
        // Fallback for pages without nav-menu but with navbar
        const navbarContainer = document.querySelector('.navbar .container');
        if (navbarContainer) {
            navbarContainer.appendChild(switcherContainer);
        } else {
            // Absolute fallback
            document.body.appendChild(switcherContainer);
        }
    }

    // Set initial value
    const savedLang = localStorage.getItem('language') || 'en';
    select.value = savedLang;

    // Add event listener
    select.addEventListener('change', (e) => {
        setLanguage(e.target.value);
    });

    // Initial translation
    initLanguage();
}
